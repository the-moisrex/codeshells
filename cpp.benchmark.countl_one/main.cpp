int ccl_mine(unsigned char a) { return utf8_skip<unsigned char>[a]; }

int ccl(unsigned char a) { return std::countl_one(a); }

volatile unsigned char num = 0b1100'0000;

static void CountLeft(benchmark::State &state) {
  for (auto _ : state) {
    auto val = ccl(num);
    benchmark::DoNotOptimize(val);
  }
}
BENCHMARK(CountLeft);

static void Table(benchmark::State &state) {
  for (auto _ : state) {
    auto val = ccl_mine(num);
    benchmark::DoNotOptimize(val);
  }
}
